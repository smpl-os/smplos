def main():
    themes_dir = THEMES_DIR
    themes = [d for d in os.listdir(themes_dir)
              if os.path.isdir(os.path.join(themes_dir, d))
              and not d.startswith("_")]
    themes.sort()

    ok = 0
    skip = 0
    for theme in themes:
        toml_path = os.path.join(themes_dir, theme, "colors.toml")
        css_path  = os.path.join(themes_dir, theme, "nemo.css")
        if not os.path.exists(toml_path):
            skip += 1
            continue
        try:
            c = read_toml_simple(toml_path)
            css = make_nemo_css(c)
            with open(css_path, "w") as f:
                f.write(css)
            print(f"  ✓ {theme}")
            ok += 1
        except Exception as e:
            print(f"  ✗ {theme}: {e}", file=sys.stderr)

    print(f"\n{ok} themes updated, {skip} skipped (no colors.toml).")


if __name__ == "__main__":
    main()
