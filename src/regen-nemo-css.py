def read_toml_simple(path):
    """Parse a flat key = "value" TOML file. No sections, no arrays."""
    d = {}
    with open(path) as f:
        for line in f:
            line = line.strip()
            if not line or line.startswith("#"):
                continue
            m = re.match(r'^(\w+)\s*=\s*"([^"]+)"', line)
            if m:
                d[m.group(1)] = m.group(2)
    return d


